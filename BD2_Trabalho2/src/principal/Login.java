    private void verificarAcesso(){
        String host = tfHost.getText();
        String porta = tfPorta.getText();
        String login = tfLogin.getText();
        String senha = new String(tfSenha.getPassword());
        
        ControlaConexao.setHost(host);
        ControlaConexao.setPorta(porta);
        ControlaConexao.setLogin(login);
        ControlaConexao.setSenha(senha);
        Connection conexao = null;
        try {
            conexao = ControlaConexao.getConexao();
        } catch (SQLException sqlEx) {
            System.out.println(sqlEx.getMessage());
        } finally {
            try {
                ControlaConexao.fecharConexao(conexao);
            } catch (BDException ex) {
                System.out.println(ex.getMessage());
            }
        }
        try {
            if (conexao != null && conexao.isClosed()) {
                JOptionPane.showMessageDialog(null, "Acesso autorizado", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                janelaPrincipal();
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Login ou senha incorreto(s)", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
    private void janelaPrincipal(){
        Principal principal = new Principal(tfLogin.getText());
        principal.setVisible(true);
    }
    
